#filter() function is a built-in function in Python that filters elements from an iterable based on a given condition.
# The syntax for the filter() function is:
# filter(function, iterable)
# where function is the function that tests each element of the iterable, and iterable is the iterable whose elements are to be filtered.
# The filter() function returns an iterator that contains only the elements from the iterable for which the function returns True.
