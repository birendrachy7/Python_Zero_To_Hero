# map() function is a built-in function in Python that applies a given function to each item of an iterable (like a list, tuple, etc.) and
# returns an iterator that yields the results. 
# The syntax for the map() function is:
# map(function, iterable, ...)
# where function is the function to be applied to each item of the iterable, 
# and iterable is the iterable whose items are to be processed.

